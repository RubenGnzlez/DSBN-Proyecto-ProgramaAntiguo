    private void mbarGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mbarGuardarActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
            "Directorios *.dir", "dir"); //nombre de archivo de como guardar figuras
        chooser.setFileFilter(filter);
        int returnVal = chooser.showSaveDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                FileOutputStream fos = new FileOutputStream(chooser.getSelectedFile());
                ObjectOutputStream oos = new ObjectOutputStream(fos);
                oos.writeObject(directorio);
                oos.close();
                fos.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
            }
        }
    }//GEN-LAST:event_mbarGuardarActionPerformed

    private void mbarAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mbarAbrirActionPerformed
        Persona p;
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
            "Directorios *.dir", "dir"); //nombre de archivo de como guardar figuras
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                FileInputStream fis = new FileInputStream(chooser.getSelectedFile());
                ObjectInputStream ois = new ObjectInputStream(fis);
                try {
                    directorio = (ArrayList) ois.readObject();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Agenda.class.getName()).log(Level.SEVERE, null, ex);
                }
                ois.close();
                fis.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                return;
            }
            p = (Persona) directorio.get(0);
            txtNombre.setText(p.getNombre());
            txtTelefono.setText(p.getTelefono());
            txtDireccion.setText(p.getDireccion());
            txtFecha.setText(p.getFecha());
        }
    }//GEN-LAST:event_mbarAbrirActionPerformed

