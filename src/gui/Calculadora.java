    private void bttnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnResultadoActionPerformed

        double r=0;
        b=Double.parseDouble(txtValores.getText());

        switch(op){
            case '-' : r=a-b;
            break;
            case '+' : r=a+b;
            break;
            case '/' :
            if(b==0){

                while(b==0){
                    Aviso.mostrar("Recuerde que las divisiones entre 0 no est√°n permitidas");
                    c = JOptionPane.showInputDialog(this,"Ingresa un numero diferente de 0");
                    b=Double.parseDouble(c);
                }
                r=a/b;
            }else{
                r=a/b;
            }
            break;
            case '*' : r=a*b;
            break;
        }
        txtValores.setText(""+r);
        Numero="";
    }//GEN-LAST:event_bttnResultadoActionPerformed

